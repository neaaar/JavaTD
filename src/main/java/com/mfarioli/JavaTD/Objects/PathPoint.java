package com.mfarioli.JavaTD.Objects;

public class PathPoint {
    int xCord;
    int yCord;

    public int getxCord() {
        return xCord;
    }

    public int getyCord() {
        return yCord;
    }

    public PathPoint(int xCord, int yCord) {
        this.xCord = xCord;
        this.yCord = yCord;
    }
}
